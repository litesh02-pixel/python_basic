"""#scatter plot"""

x=np.linspace(0,10,50)
y=np.sin(x)
z=np.cos(x)
fig2=plt.figure()
ax=fig2.add_axes([0,0,1,1])
ax.scatter(x,y,color='r')
ax.scatter(x,z,color='b')
plt.show()

