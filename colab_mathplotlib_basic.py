#importing mathplotlib library
import matplotlib.pyplot as plt

import numpy as np
import pandas as pd

x=np.linspace(1,10,100)
print(x)
y=np.sin(x)
z=np.cos(x)
print(y)
print(z)

#plotting the data:
#sine wave
plt.plot(x,y)
plt.show()

#cos wave
plt.plot(x,z)
plt.show()

#x-label and y-label and title on graph
plt.plot(x,y)
plt.xlabel('angles')
plt.ylabel('sine values')
plt.title('sine wave')
plt.show()

