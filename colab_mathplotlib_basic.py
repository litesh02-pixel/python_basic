"""#Pie chart"""

fig1=plt.figure()
ax=fig1.add_axes([0,0,1,1])
language=['english','hindi','spanish','latin','marathi']
people=[100,50,150,40,70]
ax.pie(people,labels=language,autopct='%1.2f%%')
